package bff;

public class $Proxy extends $.Id {
    final $ proxied;
    public $Proxy($ proxied) {
        this.proxied = proxied;
    }

    public Object eval(Scope s) { return proxied.eval(s); }
    public Object eval(Scope s, Object o) { return proxied.eval(s, o); }
    public Object eval(Scope s, Object o1, Object o2) { return proxied.eval(s, o1, o2); }
    public Object eval(Scope s, Object o1, Object o2, Object o3) { return proxied.eval(s, o1, o2, o3); }
    public Object eval(Scope s, Object o1, Object o2, Object o3, Object o4) { return proxied.eval(s, o1, o2, o3, o4); }
    public Object eval(Scope s, Object o1, Object o2, Object o3, Object o4, Object o5) { return proxied.eval(s, o1, o2, o3, o4, o5); }
    public Object eval(Scope s, Object o1, Object o2, Object o3, Object o4, Object o5, Object o6) { return proxied.eval(s, o1, o2, o3, o4, o5, o6); }
    public Object eval(Scope s, Object o1, Object o2, Object o3, Object o4, Object o5, Object o6, Object o7) { return proxied.eval(s, o1, o2, o3, o4, o5, o6, o7); }
    public Object eval(Scope s, Object o1, Object o2, Object o3, Object o4, Object o5, Object o6, Object o7, Object o8) { return proxied.eval(s, o1, o2, o3, o4, o5, o6, o7, o8); }
    public Object eval(Scope s, Object o1, Object o2, Object o3, Object o4, Object o5, Object o6, Object o7, Object o8, Object o9) { return proxied.eval(s, o1, o2, o3, o4, o5, o6, o7, o8, o9); }
    public Object eval(Scope s, Object o1, Object o2, Object o3, Object o4, Object o5, Object o6, Object o7, Object o8, Object o9, Object o10) { return proxied.eval(s, o1, o2, o3, o4, o5, o6, o7, o8, o9, o10); }
    public Object eval(Scope s, Object o1, Object o2, Object o3, Object o4, Object o5, Object o6, Object o7, Object o8, Object o9, Object o10, Object... rest) { return proxied.eval(s, o1, o2, o3, o4, o5, o6, o7, o8, o9, o10, rest); }

}
